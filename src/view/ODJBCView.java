    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        String kolom = "";
        switch (category.getSelectedIndex()) {
            case 0:
                kolom = "grade_level";
                break;
            case 1:
                kolom = "lowest_sal";
                break;
            case 2:
                kolom = "highest_sa;";
                break;
            default:
                throw new AssertionError();
        }

        jgc.bindingSearch(tblJobGrade, header, kolom, tfcari.getText());
//        System.out.println(category.getSelectedItem().toString());
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        boolean hasil = false;
        if (!tfTingkat.isEnabled()) {
            hasil = jgc.update(tfTingkat.getText(), Integer.parseInt(tfGajiR.getText()), Integer.parseInt(tfGajiT.getText()));
            System.out.println(category.getSelectedItem().toString());
        } else {
            hasil = jgc.insert(tfTingkat.getText(), Integer.parseInt(tfGajiR.getText()), Integer.parseInt(tfGajiT.getText()));
            System.out.println(category.getSelectedItem().toString());
        }
        String pesan = "Gagal menambahkan data";
        if (hasil) {
            pesan = "Berhasil menambahkan data";
        }
        JOptionPane.showMessageDialog(rootPane, pesan);
        jgc.bindingALL(tblJobGrade, header);
        reset();

    }//GEN-LAST:event_btnSimpanActionPerformed

