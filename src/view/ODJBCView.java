    private void tblJobGradeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblJobGradeMouseClicked
        // TODO add your handling code here:
        tfTingkat.setText("" + tblJobGrade.getValueAt(tblJobGrade.getSelectedRow(), 0) + "");
        tfGajiR.setText("" + tblJobGrade.getValueAt(tblJobGrade.getSelectedRow(), 1) + "");
        tfGajiT.setText("" + tblJobGrade.getValueAt(tblJobGrade.getSelectedRow(), 2) + "");
        tfTingkat.setEnabled(false);
        tfGajiR.setEnabled(true);
        tfGajiT.setEnabled(true);
        btnSimpan.setEnabled(true);
        btnHapus.setEnabled(true);
    }//GEN-LAST:event_tblJobGradeMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int i = JOptionPane.showConfirmDialog(this, "Apakah anda yakin?");
        if (i == 0) {
            String pesan = "Gagal Hapus";
            boolean hasil = jgc.delete(tfTingkat.getText());
            if (hasil) {
                pesan = "Berhasil Hapus";
            }
            JOptionPane.showMessageDialog(this, pesan);
            jgc.bindingALL(tblJobGrade, header);
        }
        reset();// TODO add your handling code here:
    }//GEN-LAST:event_btnHapusActionPerformed

